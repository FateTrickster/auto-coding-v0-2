@app.command("init-from-codebook")
def init_from_codebook_cli(
    project_dir: str = typer.Option(..., help="Project output directory"),
    initial_codebook: str = typer.Option(..., help="Path to initial_codebook.md"),
    unit_table: str = typer.Option(..., help="Path to unit_table.csv"),
    config: str = typer.Option(None, help="Path to project_config.yaml"),
):
    """Initialize project directory from input files. Pilot sample is now auto-generated by sample-pilot."""
    # Validate all inputs exist BEFORE creating any directories
    if not Path(initial_codebook).exists():
        raise FileNotFoundError(f"initial_codebook not found: {initial_codebook}")
    if not Path(unit_table).exists():
        raise FileNotFoundError(f"unit_table not found: {unit_table}")
    if config and not Path(config).exists():
        raise FileNotFoundError(f"config file not found: {config}")

    from .io_utils import ensure_dir
    proj = ensure_dir(project_dir)
    inputs_dir = ensure_dir(proj / "00_inputs")
    shutil.copy2(initial_codebook, inputs_dir / "initial_codebook.md")
    shutil.copy2(unit_table, inputs_dir / "unit_table.csv")
    if config:
        shutil.copy2(config, inputs_dir / "project_config.yaml")
    for sub in ["01_codebook", "02_prompts", "03_units", "04_pilot"]:
        ensure_dir(proj / sub)
    print(f"Project initialized: {proj}")


