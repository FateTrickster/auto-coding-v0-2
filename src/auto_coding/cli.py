@app.command("sample-pilot")
def sample_pilot_cli(
    project_dir: str = typer.Option(..., help="Project directory"),
    target_size: int = typer.Option(300, help="Target sample size"),
    seed: int = typer.Option(42, help="Random seed"),
    risk_config: str = typer.Option(None, help="风险画像 YAML（仅适用于 round_02 及以后）"),
    control_group: str = typer.Option(None, help="需要在 pilot 中超采样的对照 group_id"),
    round_id: str = typer.Option("round_01", help="Round identifier (round_01, round_02, ...)"),
):
    """Generate pilot sample. Round 1 uses only structural fields; Round 2+ can use risk profile."""
    from .pilot_sampler import sample
    round_num = _parse_round_number(round_id)

    rc = Path(risk_config) if risk_config else None
    if round_num == 1 and rc:
        raise typer.BadParameter(
            "Round 1 must not use --risk-config. Risk profiles are only valid for round_02 and later."
        )
    if rc and not rc.exists():
        raise FileNotFoundError(f"Risk config not found: {rc}")

    unit_path = Path(project_dir) / "03_units" / "unit_table_v0.1.csv"
    if not unit_path.exists():
        raise FileNotFoundError(
            f"Validated unit table not found: {unit_path}\n"
            f"Run `validate-units` first."
        )

    r = sample(
        unit_path,
        Path(project_dir)/"04_pilot",
        target_size=target_size,
        seed=seed,
        risk_config_path=rc,
        control_group=control_group,
    )
    print(f"Sampled {r['sampled_count']} units, groups={r['groups_covered']}, "
          f"risk_config={'YES' if r['risk_config_used'] else 'NO'}")


def _parse_round_number(round_id: str) -> int:
    """Parse round_01 → 1, round_02 → 2. Rejects invalid formats."""
    import re
    m = re.fullmatch(r"round_(\d{2,})", round_id)
    if not m:
        raise typer.BadParameter(f"Invalid round_id format: {round_id!r}. Expected 'round_NN'.")
    n = int(m.group(1))
    if n < 1:
        raise typer.BadParameter(f"Round number must be >= 1, got {n} from {round_id!r}.")
    return n


